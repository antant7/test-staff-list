-- Проверка данных
SELECT
    COUNT(*) as общее_количество_записей,
    COUNT(CASE WHEN pid = 0 THEN 1 END) as главных_руководителей,
    COUNT(DISTINCT email) as уникальных_email,
    COUNT(DISTINCT home_phone) as уникальных_телефонов
FROM staff;

-- Показать структуру иерархии
SELECT
    pid,
    COUNT(*) as количество_сотрудников
FROM staff
GROUP BY pid
ORDER BY pid;
