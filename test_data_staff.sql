DELIMITER //

CREATE PROCEDURE GenerateStaffData()
BEGIN
    DECLARE i INT DEFAULT 1;
    DECLARE manager_id INT;
    DECLARE first_names TEXT DEFAULT 'Александр,Елена,Дмитрий,Мария,Сергей,Анна,Павел,Ольга,Игорь,Наташа,Виктор,Светлана,Алексей,Ирина,Роман,Татьяна,Андрей,Юлия,Михаил,Екатерина,Николай,Анастасия,Владимир,Оксана,Максим,Дарья,Артем,Полина,Денис,Алина,Константин,Вера,Станислав,Людмила,Евгений,Галина,Олег,Марина,Вадим,Лариса,Руслан,Жанна,Кирилл,Надежда,Антон,Валентина,Глеб,Инна,Леонид,Тамара';
    DECLARE last_names TEXT DEFAULT 'Петров,Сидоров,Козлов,Волков,Морозов,Федоров,Новиков,Соколов,Лебедев,Попов,Орлов,Андреев,Смирнов,Кузнецов,Васильев,Михайлов,Иванов,Романов,Сергеев,Егоров,Степанов,Николаев,Григорьев,Лазарев,Медведев,Ершов,Никитин,Соловьев,Алексеев,Макаров,Марков,Фролов,Матвеев,Давыдов,Крылов,Калинин,Титов,Карпов,Куликов,Антонов,Воронов,Захаров,Жуков,Борисов,Комаров,Наумов,Шилов,Воробьев,Сазонов,Тарасов';
    DECLARE positions TEXT DEFAULT 'Старший разработчик,Младший разработчик,Средний разработчик,Инженер-программист,Системный администратор,Администратор БД,Бизнес-аналитик,Проектный менеджер,Технический писатель,UI/UX дизайнер,Бухгалтер,Финансовый аналитик,Специалист по маркетингу,Менеджер по продажам,Служба поддержки,Операционный специалист,HR специалист,Офис-менеджер,Сотрудник безопасности,Обслуживающий персонал';

    DECLARE first_name_count INT DEFAULT 50;
    DECLARE last_name_count INT DEFAULT 50;
    DECLARE position_count INT DEFAULT 20;

    DECLARE fname VARCHAR(50);
    DECLARE lname VARCHAR(50);
    DECLARE pos VARCHAR(150);
    DECLARE email_addr VARCHAR(255);
    DECLARE phone VARCHAR(20);
    DECLARE note TEXT;

    -- Генерация сотрудников для каждого отдела
    WHILE i <= 9850 DO
        -- Выбор случайного менеджера (из всех менеджеров: ids 7-150)
        SET manager_id = 7 + (i % 144);

        -- Генерация случайных имен
        SET fname = TRIM(SUBSTRING_INDEX(SUBSTRING_INDEX(first_names, ',', 1 + (i % first_name_count)), ',', -1));
        SET lname = CONCAT(
            TRIM(SUBSTRING_INDEX(SUBSTRING_INDEX(last_names, ',', 1 + (i % last_name_count)), ',', -1)),
            CASE
                WHEN i % 7 = 0 THEN 'енко'
                WHEN i % 11 = 0 THEN 'ский'
                WHEN i % 13 = 0 THEN 'ич'
                ELSE ''
            END
        );

        SET pos = TRIM(SUBSTRING_INDEX(SUBSTRING_INDEX(positions, ',', 1 + (i % position_count)), ',', -1));

        -- Генерация уникального email
        SET email_addr = CONCAT(
            LOWER(SUBSTRING(fname, 1, 1)),
            '.',
            LOWER(lname),
            '.',
            i,
            '@company.com'
        );

        -- Генерация уникального телефона
        SET phone = CONCAT(
            '+7-',
            CASE (i % 10)
                WHEN 0 THEN '495'
                WHEN 1 THEN '499'
                WHEN 2 THEN '812'
                WHEN 3 THEN '383'
                WHEN 4 THEN '343'
                WHEN 5 THEN '846'
                WHEN 6 THEN '863'
                WHEN 7 THEN '391'
                WHEN 8 THEN '351'
                ELSE '473'
            END,
            '-',
            LPAD(100 + (i % 900), 3, '0'),
            '-',
            LPAD(1000 + (i % 9000), 4, '0')
        );

        -- Генерация краткой заметки
        SET note = CASE (i % 8)
            WHEN 0 THEN 'Опытный сотрудник'
            WHEN 1 THEN 'Новый член команды'
            WHEN 2 THEN 'Удаленный работник'
            WHEN 3 THEN 'Сотрудник на неполный день'
            WHEN 4 THEN 'Координатор команды'
            WHEN 5 THEN 'Участник проекта'
            WHEN 6 THEN 'Специалист отдела'
            ELSE 'Обычный сотрудник'
        END;

        INSERT INTO staff (pid, first_name, last_name, position, email, home_phone, notes)
        VALUES (manager_id, fname, lname, pos, email_addr, phone, note);

        SET i = i + 1;
    END WHILE;
END//

DELIMITER ;

-- Выполнение процедуры для генерации данных
